                            <h4 id="pregunta4">4 ¿Qué mantenimiento debo hacer y cuándo?</h4>
                            <div>
                                Estas son recomendaciones de servicio genéricos basadas únicamente en el tiempo o el kilometraje, no en la apariencia visual o la medición (tanteo). Su propósito es prolongar la vida útil de su vehículo y ayudar a prevenir averías. Los requisitos de su vehículo pueden diferir según los hábitos de manejo y/o las recomendaciones del manual del propietario. Consulte el manual del propietario y con su mecánico de automóviles para obtener información específica. aceite - lubricante - filtro: 5000 km a 8000 km rotación de llantas: 5,00 a 8,000 km dependiendo del estilo de llanta válvula pcv: anualmente, reemplace y dé servicio al sistema pcv diagnóstico completo del coche: anualmente y antes de la compra o venta filtro de combustible: anualmente o según sea necesario servicio de batería: anualmente o según sea necesario aire acondicionado: comprobar cada primavera o cuando sea necesario refrigerante del radiador: cada dos años, enjuague lavado del sistema de frenos: cada año lavado del sistema de embrague: cada año tapa del radiador: reemplazar cada dos años lavado de aceite del motor: 2 años o 50,000 km cambio de aceite automática de la transmisión: 2 años o 40,000 km lavado de dirección asistida: 2 años o 40,000 km servicio del sistema egr: 2 años o 40,000 km servicio del cuerpo del acelerador: 2 años o 50,000 millas o según sea necesario mangueras de radiador y calentador: 4 años o 100,000 km correas de transmisión: 4 años o 100,000 km correa de distribución: 4 años o 100,000 km líquido diferencial: 25,000 a 80,000 km servicio trans manual: 25,000 a 80,000 km válvulas: ajuste según el manual del propietario escanear los códigos de la computadora: cuando la luz ámbar "check engine" está encendida sensor de oxígeno: según el manual del propietario o según sea necesario
                            </div>

                            <h4 id="pregunta1">1 ¿Porque son caros?</h4>
                            <div>
                                No somos caros, tenemos dos situaciones, la primera es que vienen de otros talleres, donde el precio es menor y el cliente obtiene una mala reparación, se tiene que invertir nuevamente con un precio mas alto (el que corresponde al servicio). La segunda es que al no hacer un mantenimiento preventivo el coche llega a la situation de requerir una reparación correctiva lo cual implica la reparación de la piezas dañadas y las que se afectaron por esta razon.
                            </div>

                            </div>
                            <br>
                            
                            <h4 id="pregunta6">6 ¿Cómo debo preparar mi coche para un viaje por carretera?</h4>
                            <div>
                                Solicita revisión de los 30 puntos de seguridad.
                            </div>
                            <br>
                            
                            <h4 id="pregunta7">7 ¿Por qué mis frenos chillan?</h4>
                            <div>
                                Los frenos pueden chillar por una variedad de razones, pero los chillidos continuos pueden significar que es hora de cambiar las las pastillas(balatas) de freno. Frenos gastados pueden significar distancias de frenado más largas y dificultad para detenerse en situaciones de emergencia. Las balatas y tambores que son demasiado delgados pueden incluso sobrecargarse y romperse. Recuerde, si nota alguno de estos síntomas, es una buena idea que revise sus frenos.
                            </div>
                            <br>
                            
                            <h4 id="pregunta8">8 ¿Por qué necesito cambiar el aceite de mi motor cada 5,000 km?</h4>
                            <div>
                                El aditivo en el aceite comienza a descomponerse tan pronto como se calienta a altas temperaturas. El motor de su vehículo alcanzará más de 200 grados casi cada vez que lo conduzca. La historia ha demostrado que la marca de 5,000 km es un buen intervalo para reemplazar el aceite del motor. Nunca querrá simplemente drenar el aceite de su motor y poner aceite nuevo sin cambiar el filtro de aceite. El filtro de aceite tendrá alrededor de un cuarto de galón de aceite. Si no cambia el filtro de aceite cuando cambia el aceite del motor de su vehículo, está combinando el aceite de motor limpio con aceite de motor deteriorado y esto disminuirá la eficacia del nuevo aceite de motor que acaba de poner en su vehículo.
                            </div>
                            <br>
                            
                            <h4 id="pregunta9">9 ¿Qué significa si se enciende la luz de "Check engine"?</h4>
                            <div>
                                Hay muchos sensores y componentes computarizados que administran el rendimiento y las emisiones del motor de su vehículo. Cuando uno de esta falla, la luz de "Check engine" se enciende. Aunque parezca que su automóvil funciona bien, es importante abordar el problema para evitar problemas a largo plazo.
                            </div>
                            <br>
                            
                            <h4 id="pregunta10">10 ¿Qué debo hacer si mi automóvil comienza a sobrecalentarse?</h4>
                            <div>
                                Este es un problema muy serio: si su automóvil se sobrecalienta durante demasiado tiempo, puede dañar su motor. ¡Lo antes posible, busque un lugar seguro para salir de la carretera y apague el motor! No intente verificar el nivel de líquido en el radiador; el líquido caliente puede causar quemaduras graves. Lo mejor que puede hacer es remolcar su automóvil a Mora Automotriz.
                            </div>
                            <br>
                            
                            <h4 id="pregunta11">11 ¿Por qué el aceite de motor es de color marrón lechoso?</h4>
                            <div>
                                El aceite de motor de color marrón lechoso es una indicación de refrigerante en el aceite. Esto puede ser causado por una junta de cabeza quemada (u otra junta), un enfriador de transmisión defectuoso o carcasas rotas. Esta condición es muy grave y debe ser revisada por un técnico de inmediato.
                            </div>
                            <br>
                            
                            <h4 id="pregunta12">12 ¿Cómo me aseguro de que la batería de mi auto tenga una buena conexión eléctrica?</h4>
                            <div>
                                Los cables y terminales de la batería deben limpiarse e inspeccionarse periódicamente para asegurarse de que proporcionen una buena conexión eléctrica
                            </div>
                            <br>
                            
                            <h4 id="pregunta13">13 ¿Debo considerar usar aceite de motor sintético en mi vehículo?</h4>
                            <div>
                                Los aceites de motor sintéticos pueden ser una buena opción para motores de alto rendimiento, turboalimentados o sobrealimentados. O para vehículos que se usan para remolcar (especialmente durante clima cálido) y vehículos que operan en climas extremadamente fríos o cálidos. Aunque son más caros que los aceites de base mineral, los aceites de motor sintéticos pueden mejorar la economía de combustible y proporcionar intervalos más largos entre cambios.
                            </div>
                            <br>
                            
                            <h4 id="pregunta14">14 ¿Cuándo debo reemplazar el filtro de combustible de mi automóvil?</h4>
                            <div>
                                Para ayudar a garantizar un rendimiento confiable y sin problemas, reemplace el filtro de combustible de su automóvil aproximadamente cada 50,000 km o según lo recomendado en el manual del propietario de su vehículo.
                            </div>
                            <br>
                            
                            <h4 id="pregunta15">15 ¿Cuándo debo cambiar mis bujías?</h4>
                            <div>
                                Para obtener la máxima economía de combustible y el máximo rendimiento del motor, sus bujías deben reemplazarse cada 30 meses o 50,000 km, a menos que su vehículo esté equipado con bujías con punta de platino de 160,000 km.
                            </div>
                            <br>
                            
                            <h4 id="pregunta16">16 ¿Necesito reemplazar un fusible quemado, qué debo hacer?</h4>
                            <div>
                                Siempre reemplace los fusibles quemados con otros del mismo amperaje (impreso en el fusible) y tenga en cuenta que, si un fusible continúa "quemándose", uno de nuestros técnicos debe revisar el circuito profesionalmente para detectar fallas.
                            </div>
                            <br>
                            
                            <h4 id="pregunta17">17 ¿Con qué frecuencia reviso el anticongelante? ¿Es necesario cambiarlo?</h4>
                            <div>
                                Debe revisar el anticongelante y el aceite al menos cada dos veces que llene gasolina en los autos más nuevos y cada vez que llene en los autos más antiguos con un alto kilometraje.
                            </div>
                            <br>
                            
                            <h4 id="pregunta18">18 ¿Cuánto duran las bujías?</h4>
                            <div>
                                Esto depende del tipo de bujías que tenga su automóvil. Es más probable que los autos más viejos (de 15 a 20 años) tengan enchufes estándar y duren entre 20,000 y 24,000 km, los modelos más nuevos hasta el año 2000 tienen punta de platino y durarán 48,000 km. Algunos 2000 y más nuevos tienen bujías con punta de iridio y recorrerán entre 144,000 y 160,000 km
                            </div>
                            <br>
                            
                            <h4 id="pregunta19">19 ¿Por qué debería pedirle que realice una inspección previa a la compra de un automóvil antes de comprarlo?</h4>
                            <div>
                                Al comprar un auto usado, es imperativo que sepas en lo que te estás metiendo. Quiere asegurarse de no obtener más de lo que esperaba. Realizamos en Mora Automotriz una inspección minuciosa tanto por dentro como por fuera. Comprobamos daños en la carrocería que puedan estar cubiertos con pasta y pintura. También nos aseguramos de que el vehículo esté en buenas condiciones mecánicas revisando el motor, el tren de transmisión, las correas y las mangueras. También hacemos un control de seguridad completo que incluye los frenos y buscamos daños y desgaste en la suspensión. No quieres sorpresas después de haber comprado el coche. No podemos ver todo, pero podemos ver mucho. Un beneficio adicional de tener una inspección profesional es que contará con información adicional cuando negocie un precio de venta.
                            </div>
                            <br>
                            
                            <h4 id="pregunta20">20 ¿Debo dar mantenimiento a mi transmisión, aunque se supone que debe tener un fluido "de por vida"?</h4>
                            <div>
                                En estos tiempos de economía en recesión, el mantenimiento preventivo está siendo suspendido por muchas personas que intentan estirar el sueldo. Lo entendemos totalmente, pero en este caso, no estamos de acuerdo, esta es la mejor opción. Un peso gastado en mantenimiento hoy bien puede ahorrarle diez pesos mañana. Muchos fabricantes están tratando de mostrar costos anuales de mantenimiento más bajos mediante el uso de lo que denominan fluidos "de por vida". Siempre nos hemos preguntado que es "de por vida”, Estamos viendo una gran cantidad de fallas en la transmisión en automóviles del grupo VAG de alta gama que podrían haberse evitado con servicios de transmisión regulares, cada 48,000 km. ¡Le sugerimos enfáticamente que realice un mantenimiento regular de su transmisión para prolongar su vida útil, ahorrar dinero y darle tranquilidad a usted y a su familia!
                            </div>
                            <br>
                            
                            <h4 id="pregunta21">21 ¿Cuánto cuesta reparar unidad mecatrónica dsg?</h4>
                            <div>
                                Los costos totales dependen de los códigos de falla que deben repararse.
                            </div>
                            <br>
                            
                            <h4 id="pregunta22">22 Tengo una unidad mecatrónica defectuosa en mi coche. ¿puedo reemplazar la unidad defectuosa con la unidad usada que se toma de un coche similar, debe funcionar en mi coche?</h4>
                            <div>
                                No, es por eso que obtendrá una versión incorrecta y causa dificultades en la capacidad de conducción, puntos de cambio incorrectos, controles sincronizados incorrectamente entre TCM y ECU, desgaste del embrague demasiado temprano y rápido, códigos de falla o la unidad mecatrónica no funcionará en absoluto, etc. ¡Es por eso que la unidad mecatrónica que se toma de otro automóvil DEBE tener la misma versión HW y DEBE volver a actualizarse con el software específico del automóvil!

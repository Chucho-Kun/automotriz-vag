                            <h4 id="pregunta2">2 ¿Porque se cobra diagnostico por sistema o área?</h4>
                            <div>
                                Nuestros vehículos tienen diferentes sistemas y complejidades: Hablemos del motor y la transmisión que son componentes clave del vehículo, pero son sistemas separados con sus propias complejidades. Diagnosticar un problema en el motor puede requerir un conjunto diferente de herramientas y conocimientos en comparación con la transmisión. Cada sistema tiene sus propios sensores y componentes específicos que deben ser evaluados. Tiempo y recursos adicionales: Diagnosticar problemas en diferentes secciones del vehículo puede requerir más tiempo y recursos. Por ejemplo, puede ser necesario desmontar ciertas partes para acceder a componentes específicos, y esto lleva tiempo y trabajo adicional. Especialización: Algunos talleres pueden tener especialistas en motores y otros en transmisiones. Cobrar por separado por cada diagnóstico permite que los mecánicos especializados se enfoquen en su área de expertise, lo que puede resultar en diagnósticos más precisos y soluciones más efectivas. Claridad en los costos: Cobrar por separado por cada sección del vehículo brinda una mayor claridad a los clientes sobre los costos asociados con el diagnóstico. Esto les permite tomar decisiones informadas sobre qué reparaciones autorizar y cuánto están dispuestos a invertir en el mantenimiento de su vehículo. Transparencia: Al desglosar los costos del diagnóstico por sección, se demuestra transparencia en la facturación. Los clientes pueden ver claramente cuánto se cobra por cada parte del proceso, lo que ayuda a evitar malentendidos y disputas. Personalización: Cada vehículo y situación pueden ser únicos, y algunos problemas pueden estar relacionados con una sección específica del vehículo. Cobrar por separado por cada diagnóstico permite un enfoque más personalizado en la solución de problemas. Inversión en equipos y capacitación: Los talleres deben invertir en equipos y capacitación específicos para diagnosticar problemas en motores, transmisiones y otras secciones del vehículo. Cobrar por separado por cada diagnóstico ayuda a cubrir estos costos. En resumen, cobrar un diagnóstico por cada sección del vehículo tiene sentido debido a las diferencias en complejidad, tiempo, recursos, especialización y costos asociados con cada sistema. Esto permite a los talleres brindar un servicio de diagnóstico más preciso y transparente a sus clientes.
                            </div>
                            <br>
                            
                            <h4 id="pregunta3">3 ¿Qué es el Mantenimiento Preventivo?</h4>
                            <div>
                                El mantenimiento preventivo es el servicio regular de su vehículo que lo ayuda a tener a su automóvil funcionando de manera eficiente y eliminando posibles problemas que pueden dejarlo varado. Los fabricantes saben que un automóvil con el mantenimiento adecuado será más confiable, más seguro, durará más y aumentará su satisfacción con su producto. El mantenimiento preventivo incluye además de sus 30 puntos de seguridad Interior del vehículo Alumbrado, intermitentes,luz de marcha,luz de curva estática (comprobar). Luces interiores, encendedores, tomas de corriente, bocina y testigos (comprobar). Cinturones de seguridad comprobar retenciones y bloqueo automático. Indicador de intervalos de cambio de aceite e inspección (reiniciar). Exterior del vehículo Sistema limpiaparabrisas y lavafaros (ajustar). Carrocería comprobar la corrosión visible. Techo corredizo (verificar ruidos, limpiar y engrasar carriles guía y limpiar derivabrisas). Parabrisas (verificar daños). Cerradura del cofre de motor(limpiar, comprobar fijación y engrasar (solo Toledo) Llantas Comprobar estado banda de rodadura presión de inflado y profundidad. Calibrar el testigo de control de lo neumaticos Bajo el vehículo Vaciar el aceite de motor, sustituir el filtro de aceite y tapón carter. Grosor de las pastilla y estado d elos discos de freno delanteros y traseros Sistema de gases de escape (verificar fijaciones, ausencia de fugas y daños). Revestimiento y protección de bajas, cableado y tapones (verificar daños) Sistema de frenos (comprobar ausencia de fugas y daños). Sistema de amortiguadores (comprobar ausencia de fugas y daños). Correa Poly-V (comprobar estado). Motor y componentes del hueco del motor (comprobacion visual de perdidas y daños). Rótulas y cojinetes de brazos oscilantes bieletas y soportes de barra estabilizadora. Muelles helicoidales y topes elásticas delante y detrás (comprobar daños visualmente). Hueco del motor Aceite de motor (rellenar y verificar nivel). Batería (comprobar estado y carga) Motor y componentes del hueco del motor (comporbacion visual de perdidas y daños). Liquido anticongelante y limpia parabrisas (verificar nivel y rellenar si es necesario). Liquido de frenos (comprobador nivel en función del desgaste de pastillas). Trabajos finales Reglaje de los faros (comprobar y realizar ajustes si es necesario). Calcomanía de próximo servicio (anotar fecha kilometraje y aplicarla ne el poste B). Herramientas de a bordo y triángulo de preseñalización (comprobar). Recorrido de prueba (sólo en caso que el cliente manifieste una falla que lo requiera).
                            </div>
